package com.learning;

public class StatementSwitch {

    public static void main(String[] args) {
        /**
         * Switch Case
         */
        int a = 11;
        switch (a) {
            case 10:
                System.out.println("Nilainya 10");
                break;

            case 20:
                System.out.println("Nilainya 20");
                break;

            default:
                System.out.println("Tidak diketahui");
                break;
        }
    }
}
